## 新年あけましておめでとうございます

皆様、新年あけましておめでとうございます。

旧年中は格別のお引き立てを賜り、誠にありがとうございました。

2026年最初の開発者ブログでは、年頭のご挨拶とともに、CoCoスモの**新しいコンセプト**についてお伝えします。

## 不動産SaaSの「あたりまえ」を問い直す

不動産SaaSと聞いて、皆様は何を思い浮かべるでしょうか。

多くの場合、**「反響を増やすためのツール」**というイメージがあるのではないでしょうか。

- ポータルサイトへの一括入稿
- SEO対策された物件ページ
- リスティング広告の最適化
- SNS自動投稿

もちろん、これらは大切です。反響がなければ、ビジネスは始まりません。

しかし——

> 「反響は来るけど、来店につながらない」
> 「内見までは行くけど、成約率が上がらない」
> 「お客様への提案に時間がかかりすぎる」

こうした声を、私たちは何度も耳にしてきました。

## 「反響後の対応」を支援するSaaSへ

2026年、CoCoスモは新しいコンセプトを掲げます。

**「反響を増やす」から「反響後の対応を支援する」へ**

具体的には、以下の指標の向上を目指します。

| 指標 | 説明 |
|------|------|
| **反響来店率** | 反響があったお客様が実際に来店する割合 |
| **反響契約率** | 反響から成約に至る割合 |
| **顧客満足度** | お客様の物件検討体験の質 |

反響を得た後、いかにお客様との関係を深め、成約につなげるか——ここにCoCoスモの価値を集中させていきます。

## 2つの新機能で実現する「反響後の対応」

このコンセプトを体現する機能として、年末年始にかけて2つの新機能をリリースしました。

### 1. お客様専用ページ（顧客限定ページ）

年末にリリースした**顧客限定ページ**は、お客様一人ひとりに専用のアクセス権を発行し、パーソナライズされた物件体験を提供する機能です。

```ruby
# お客様ごとに専用リンクを発行
customer_access = CustomerAccess.create!(
  property_publication: publication,
  customer_name: "山田太郎様",
  customer_message: "ご希望の条件にぴったりの物件です！"
)
```

#### 主な特徴

- **申し送り事項**: 営業担当からのメッセージを表示
- **AIチャット**: お客様自身で周辺施設を調べられる
- **経路追加機能**: お客様が通勤先などの経路を自分で追加
- **閲覧トラッキング**: お客様の関心度を把握

「物件を紹介する」だけでなく、**「お客様の検討をサポートする」**ツールです。

### 2. 営業プレゼンページ（説明特化型プレゼンURL）

そしてこの新年、新たにリリースしたのが**営業プレゼンページ**です。

ZoomやGoogle Meetでのオンライン商談、店頭でのタブレット説明——こうした場面で、営業担当が**お客様に物件を説明するための専用ページ**です。

#### 5ステップ構成

物件説明の「王道フロー」を5つのステップで構成しています。

```
Step 1: 周辺環境
  ↓ 「次に見る」ボタン
Step 2: 建物概要
  ↓
Step 3: 部屋詳細
  ↓
Step 4: VRルームツアー
  ↓
Step 5: 重要事項・注意点
```

営業担当のトークと画面が**自然に同期**するよう設計されています。

#### 「次に見る」ボタンで進行をコントロール

```jsx
// ステップ進行のイメージ
const SalesPresentation = () => {
  const [currentStep, setCurrentStep] = useState(0);
  const steps = ['neighborhood', 'building', 'room', 'vr', 'notes'];

  return (
    <div>
      <StepContent step={steps[currentStep]} />
      <Button onClick={() => setCurrentStep(prev => prev + 1)}>
        次に見る →
      </Button>
    </div>
  );
};
```

お客様のペースに合わせて、説明を進められます。

#### オンライン商談に最適

- **画面共有向けレイアウト**: 大きな文字、シンプルなデザイン
- **モバイル対応**: タブレットでも快適に操作
- **パスワード保護**: 商談後のURL漏洩を防止
- **有効期限**: 商談終了後に自動で無効化

## 2つのページの使い分け

| | お客様専用ページ | 営業プレゼンページ |
|---|---|---|
| 主な用途 | お客様が自分で物件を検討 | 営業担当がお客様に説明 |
| 使用シーン | 内見後のフォローアップ | Zoom商談、店頭説明 |
| 操作する人 | お客様 | 営業担当 |
| 閲覧スタイル | 自由に回遊 | ステップ順に進行 |
| AIチャット | あり | なし（営業が説明） |
| 経路追加 | お客様が追加可能 | 事前登録のみ |

**「お客様に自由に見てもらうページ」**と**「営業がお客様と一緒に見るページ」**——目的に応じて使い分けてください。

## 反響後の対応を、もっと簡単に

これらの機能は、以下のような課題を解決します。

### Before（従来）

1. 反響があったお客様にメールで物件資料を送付
2. お客様から「○○についてもっと知りたい」と質問
3. 営業担当が追加資料を作成して送付
4. 内見日程を調整
5. 内見時に改めて物件を説明
6. 内見後、お客様が検討（情報が分散）

### After（CoCoスモ活用）

1. 反響があったお客様に**顧客限定ページ**を発行
2. お客様が**AIチャット**で気になる点を自分で調べる
3. お客様が**経路**を追加して通勤シミュレーション
4. 内見日程を調整
5. 内見時（またはオンライン）で**営業プレゼンページ**を使って説明
6. 内見後も**顧客限定ページ**でじっくり検討

**お客様の「知りたい」にすぐ応えられる**仕組みで、来店率・成約率の向上を目指します。

## 今後の展望

2026年、CoCoスモはさらに「反響後の対応」を強化していきます。

### 検討中のアップデート

- **AI提案機能**: お客様の閲覧履歴から、関心の高い物件を自動提案
