## はじめに — 「何ができるか」ではなく「誰の何を解決するか」

CoCoスモのランディングページは、これまで「物件管理」「AI画像生成」「VRツアー」「簡単埋め込み」「マルチテナント」「管理者機能」という6つの機能を前面に打ち出していました。

```
旧構成:
  物件管理 → AI画像生成 → VRルームツアー → 簡単埋め込み → マルチテナント → 管理者機能
```

技術的にはCoCoスモの機能を正しく紹介していましたが、いくつかの課題がありました。

- **技術寄りの訴求**で、「誰のどんな課題を解決するか」が伝わりにくい
- 反響対応・顧客管理といった**営業支援の側面**が表現されていない
- 不動産会社の担当者が最初に抱える悩み ——「反響が来たけど対応が追いつかない」「顧客情報がバラバラ」—— に刺さらない

今回、メッセージングの軸を**「反響が来てから成約するまでのプロセスを丸ごと支援」**に転換しました。

## 新しいページ構成

### コピーの変更

ページの最も目立つ箇所のコピーを、反響対応軸に書き換えました。

| 箇所 | 旧 | 新 |
|------|-----|-----|
| DXバナー | 不動産業務をDXで変革 | **反響対応を、もっとスマートに** |
| キャッチコピー | AI画像生成、VRツアー作成、物件管理を一つのプラットフォームで。 | **反響の受付から顧客対応、物件提案、成約まで。** |
| サブコピー | 不動産会社の業務効率を劇的に向上させます。 | **不動産の営業プロセスをワンストップで支援します。** |

ポイントは、「何ができるか（機能の列挙）」ではなく「どんなプロセスをカバーするか（業務フロー）」に焦点を当てたことです。不動産会社の方が読んだときに、自社の営業フローと重ね合わせてイメージできることを意識しています。

### 実績セクションの微調整

```
