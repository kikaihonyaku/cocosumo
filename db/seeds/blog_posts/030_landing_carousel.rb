## 機能紹介の再構成 — 営業フローに沿った6機能

旧構成では技術カテゴリごとに機能を並べていましたが、新構成では**反響対応の業務フローに沿った順序**で再定義しました。

```
旧: 物件管理 → AI画像生成 → VRツアー → 簡単埋め込み → マルチテナント → 管理者機能

新: 反響自動取込 → 顧客・案件管理 → 顧客マイページ → VR＆ステージング → AI編集 → GISマップ
    ──────────────────────────────────────────────────────────────
    反響受付        顧客対応         物件提案          コンテンツ作成      物件管理
```

| # | 機能名 | 対応する営業フェーズ | 変更内容 |
|---|--------|------------------|---------|
| 1 | 反響自動取込 | 反響受付 | **新規追加** |
| 2 | 顧客・案件管理 | 顧客対応 | **新規追加** |
| 3 | 顧客マイページ | 物件提案 | **新規追加** |
| 4 | VRツアー＆バーチャルステージング | 物件提案 | 旧VRツアーと統合 |
| 5 | AI画像編集＆コンテンツ作成 | コンテンツ作成 | 旧AI画像生成を拡張 |
| 6 | GISマップ物件管理 | 物件管理 | 旧物件管理をリネーム |

新しく追加した3つの機能（反響自動取込、顧客・案件管理、顧客マイページ）は、CoCoスモがすでに実装している機能です。以前のランディングページでは訴求されていなかっただけで、今回の刷新でようやく表舞台に出ました。

