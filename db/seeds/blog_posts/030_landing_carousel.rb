## FAQの追加

反響対応に関する質問を1つ追加しました。

> **Q: ポータルサイトからの反響は自動で取り込めますか？**
>
> A: はい、SUUMOなどのポータルサイトからの反響通知メールを自動で取り込みます。顧客の名前・連絡先を自動抽出し、案件として登録されます。転送設定をするだけで、手入力は不要です。

また、既存のQ2（導入時間に関する質問）の回答にあった「SUUMOからの自動インポート」の記述を「反響メール自動取込」に変更し、メッセージングの一貫性を確保しています。

## 実装のポイント — 変更はすべて1ファイル

今回の刷新で変更したファイルは `app/frontend/pages/Landing.jsx` の1つだけです。ランディングページの構成をデータドリブン（`features` / `slides` / `stats` / `faqs` 配列）にしていたことが功を奏し、コピーと構成の変更がデータ配列の差し替えだけで完結しました。

```
変更内容:
├── Icons オブジェクト ← Mail, Map, Chart アイコン追加
├── features 配列    ← 6機能を再定義
├── slides 配列      ← カルーセルを features に連動更新
├── stats 配列       ← 数値ラベル変更
├── faqs 配列        ← 1件追加、1件修正
├── DXバナー         ← コピー変更
├── キャッチコピー    ← コピー変更
├── 料金セクション    ← 機能リスト変更
└── 新セクション      ← 「選ばれる理由」3カラム追加
```

コンポーネントの構造（DemoSlideshow, FeatureCarousel, FAQItem, BlogSection）やEmbla Carouselの実装には一切手を加えていません。UIの「器」を変えずに「中身」だけを入れ替えた形です。

## まとめ

今回のランディングページ刷新は、技術的な大改修ではなく**メッセージングの転換**です。

- **訴求軸**: 「ツールの機能紹介」→「営業プロセスの支援」
- **構成順序**: 技術カテゴリ順 → 反響対応の業務フロー順
- **新セクション**: 「CoCoスモが選ばれる理由」で3つの価値を端的に伝達
- **一貫性**: コピー・機能紹介・カルーセル・料金・FAQを一括で連動更新

ランディングページは、見込み顧客がCoCoスモに出会う最初の接点です。「このサービスは何のツールなんだろう？」ではなく、「反響対応の課題、うちもあるな」と思ってもらえるページに生まれ変わりました。
